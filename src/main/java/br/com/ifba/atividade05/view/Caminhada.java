    private void BtnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnCalcularActionPerformed
        // TODO add your handling code here:
        
        int ValorInicio = (Integer)SLinicio.getValue();// pega os valores do slider
        int ValorFinal = (Integer)SLfinal.getValue();// pega os valores do slider
        int Passos = (Integer)SLpasso.getValue();// pega os valores do slider
         
        if(ValorFinal < ValorInicio){ //caso o inicio seja maior que  o final 
         JOptionPane.showMessageDialog(null,"Valor de inicio maior que o valor final, tente novamente","ERROR",JOptionPane.ERROR_MESSAGE);//uso de tela de erro ja pronta da biblioteca
        }else{
            TextApassos.setText("");// limpa a tela a cada vez que tenta novamente
            for(int i = ValorInicio ; i <= ValorFinal; i = i + Passos ){ // logica envolvida no calculo de passos
                TextApassos.setText(TextApassos.getText()+ "\n" + i); // imprime os passos no textArea 
            }
        }
    }//GEN-LAST:event_BtnCalcularActionPerformed

