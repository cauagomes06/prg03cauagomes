    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        // TODO add your handling code here:
        
         String tipoPagamento = (String)cbEscolhaPagamento.getSelectedItem();// pega o tipo de pagamento da comboBox e coloca numa string
         double valorCompra = (Integer)spValorProduto.getValue(); // pega um valor inteiro de um spinner
         if( valorCompra <= 0 ){ // verificacao
             JOptionPane.showMessageDialog(null,"Valor invalido","ERROR",JOptionPane.ERROR_MESSAGE);
         }else{
         Pagamento pagamento = null; // cria a variavel do tipo "Pagamento" e iniciliza com null

        switch(tipoPagamento){ // verifica de que tipo selecionado na COMBOBOX e escolhe a correta

            case "Dinheiro" ->

            pagamento = new PagamentoDinheiro(); //se for Dinheiro cria um objeto da classe PagamentoDinheiro
            case "Cartao"->

            pagamento = new PagamentoCartao();//se for Cartao cria um objeto da classe PagamentoCartao
            case "Pix" -> 
          
            pagamento = new PagamentoPix();  //se for Pix cria um objeto da classe PagamentoPix
        }

        if(pagamento!= null){ // se nao ocorrer nenhum erro

              double valorFinal = pagamento.calcularTotal(valorCompra); // criae variavel valor final e preenche usando o metodo de calcularToatal
             lblResultado.setText(pagamento.imprimirRecibo(valorCompra, valorFinal));// Imprimi o recibo no LABEL
        }
      }
    }//GEN-LAST:event_btnCalcularActionPerformed

